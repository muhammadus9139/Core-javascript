// If Else Statement
let age = 20;

// Simple if
if (age >= 18) {
    console.log("Aap adult hain");
}

// if-else
if (age >= 18) {
    console.log("Aap vote de sakte hain");
} else {
    console.log("Aap vote nahi de sakte");
}

// if-else if-else
let marks = 75;

if (marks >= 90) {
    console.log("Grade: A+");
} else if (marks >= 80) {
    console.log("Grade: A");
} else if (marks >= 70) {
    console.log("Grade: B");
} else if (marks >= 60) {
    console.log("Grade: C");
} else {
    console.log("Grade: Fail");
}
