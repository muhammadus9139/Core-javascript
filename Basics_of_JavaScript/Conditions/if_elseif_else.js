// If ElseIf Else
let day = "Monday";

if (day === "Monday") {
    console.log("Hafta shuru hua");
} else if (day === "Friday") {
    console.log("Weekend aa gaya!");
} else if (day === "Saturday" || day === "Sunday") {
    console.log("Chhutti hai!");
} else {
    console.log("Normal din hai");
}

// Number example
let score = 85;

if (score >= 90) {
    console.log("Excellent!");
} else if (score >= 80) {
    console.log("Very Good!");
} else if (score >= 70) {
    console.log("Good!");
} else if (score >= 60) {
    console.log("Average");
} else {
    console.log("Fail");
}
